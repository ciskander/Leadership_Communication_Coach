# Type alias for raw parsed turns: (speaker, text, start_time_sec)
RawTurn = tuple[str, str, Optional[float]]

# Boilerplate patterns to strip
_BOILERPLATE_RE = re.compile(
    r"(Transcribed by Otter\.ai|DISCLAIMER:|This transcript was produced|"
    r"Auto-generated by|Powered by Rev\.com|www\.[a-z]+\.com)",
    re.IGNORECASE,
)

_HTML_TAG_RE = re.compile(r"<[^>]+>")
_VOICE_TAG_RE = re.compile(r"<v ([^>]+)>")  # <v Speaker Name>
_TIMESTAMP_INLINE_RE = re.compile(r"\b\d{1,2}:\d{2}(:\d{2})?(\.\d+)?\b")
_HEADER_LINE_RE = re.compile(
    r"^(Meeting(\s*(#|Type|Name|Title))?|Leader|Role|Participants|Date|Location|Duration|Agenda|Subject|Topic|Project"
    r"|Customer|Call\s*Title|Time(\s*/\s*Time\s*Zone)?|Time\s*Zone|Company|Attendees|Facilitator"
    r"|Prepared\s*by|Recorded\s*by|Type|Title|Reference|Notes|Duration)\s*:",
    re.IGNORECASE,
)

# VTT header / special block markers
_VTT_BLOCK_SKIP_RE = re.compile(r"^(STYLE|NOTE|REGION)\b", re.IGNORECASE)

# SRT index lines (digit-only lines)
_SRT_INDEX_RE = re.compile(r"^\d+$")
# SRT bracketed speaker: "[Speaker Name] text"
_SRT_BRACKET_SPEAKER_RE = re.compile(r"^\[([^\]]{1,60})\]\s*(.*)")
# SRT/VTT timecode
_TIMECODE_RE = re.compile(
    r"\d{2}:\d{2}:\d{2}[.,]\d{3}\s*-->\s*\d{2}:\d{2}:\d{2}[.,]\d{3}"
)


